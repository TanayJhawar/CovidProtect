  void checkAndPass() {
    if (city_id != null) {
      Navigator.pushReplacement(
          context,
          MaterialPageRoute(
              builder: (context) => SelectResources(
                    city: city_id,
                  )));
    } else {
      _showDialog();
    }
  }

  void _showDialog() {
    showDialog(
        context: context,
        builder: (BuildContext context) {
          return AlertDialog(
            title: new Text("Select City"),
            content: new Text("Please select your city to proceed"),
            actions: <Widget>[
              FlatButton(
                child: new Text("OK"),
                onPressed: () {
                  Navigator.pushReplacement(
                      context, MaterialPageRoute(builder: (context) => App()));
                },
              ),
            ],
          );
        });
  }
